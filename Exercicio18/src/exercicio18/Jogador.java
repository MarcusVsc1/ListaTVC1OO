public class Jogador {
    private String nome;
    private char carac;

    public Jogador(String nome, char carac) {
        this.nome = nome;
        this.carac = carac;
    }

    public String getNome() {
        return nome;
    }

    public char getCarac() {
        return carac;
    }
    
    
}
